package com.torontocodingcollective.oi;

/**
 * A Trigger on a GameController or GameJoystick
 *
 */
public enum TTrigger {

    /** The Left trigger on a GameController */
    LEFT,
    /** The Right trigger on a GameController */
    RIGHT,
    /** The Primary joystick trigger on a GameJoystick */
    CENTER,
    /** The Slider trigger on a GameJoystick */
    SLIDER
}
